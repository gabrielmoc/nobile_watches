// Função auxiliar para filtrar produtos por marca
export const getProductsByBrand = (brand: string): Product[] => {
  return mockProducts.filter(
    product => product.brand.toLowerCase() === brand.toLowerCase()
  );
};

// Função para obter opções de filtros com base nos produtos
export const getFilterOptions = (products: Product[]) => {
  return {
    brands: [...new Set(products.map(p => p.brand))],
    models: [...new Set(products.map(p => p.model))],
    caseMaterials: [
      ...new Set(products.map(p => p.caseMaterial).filter(Boolean)),
    ] as string[],
    strapMaterials: [
      ...new Set(products.map(p => p.strapMaterial).filter(Boolean)),
    ] as string[],
    dialColors: [...new Set(products.map(p => p.dialColor).filter(Boolean))] as string[],
    movements: [...new Set(products.map(p => p.movement).filter(Boolean))] as string[],
    conditions: [...new Set(products.map(p => p.condition).filter(Boolean))] as string[],
    priceRange: {
      min: Math.min(...products.map(p => p.price)),
      max: Math.max(...products.map(p => p.price)),
    },
  };
};
